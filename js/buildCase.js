$(document).ready(function(){
    // click li
    showItem('cpu');
    function showItem(typeName){
        let node;
        let items_r = $('.content__main--right ul');
        items_r.empty();
        for(i = 0; i < linh_kien.length; i++){
            if(linh_kien[i].type == typeName){
                node = `
                <li class="content__main--list-item" id='pr${linh_kien[i].img}'>
                    <div>
                        <img src="assets/product/header-product${linh_kien[i].img}.jpg" alt="laptop" class="content__main--list-item-img">
                    </div>
                    <div class="content__main--list-item-name">
                        <span class="content__main--list-item-link">
                            ${linh_kien[i].name}
                        </span>
                    </div>
                    <div class="content__main--list-item-price">
                        
                    <span class="content__main--list-item-second-price">
                            ${linh_kien[i].secondPrice}
                    </span>
                    <span class="content__main--list-item-first-price">
                            ${linh_kien[i].firstPrice}
                    </span>
                    </div>
                    <span>
                        <img src="assets/img/hotdeal8.png" class="content__main--list-item-logo">
                    </span>
                
                </li>
                `
                items_r.append(node);
            }
            
        }
    }
    // show items (right)
    let typeName = $('.content__main--left-list .content__main--left-list-item:eq(0)');
    let ls = $('.content__main--left-list .content__main--left-list-item:eq(0)');
    console.log(typeName, ls);
    $('.content__main--left-list .content__main--left-list-item').click(function(){
        typeName = this.id;
        ls = $(this);
        console.log(typeName);
        showItem(typeName);
        events();
    })
    // empty (left)
    function eventDel(){
        $('.item-r-del').click(function(){
            $(this).parents('.content__main--left-list-item').empty();
        })
    }
    events();
    function events(){
        proBuild()
        eventDel();
        eventUpdate()
    }

    // render items (list)
    function proBuild(){
        $('.content__main--right ul .content__main--list-item').click(function(){
            let Imgproduct = this.id;
            Imgproduct = Imgproduct.replace(/[a-z]|[A-Z]/igm,"");
            console.log(Imgproduct);
            let node = `
                <div class="content__main--left-list-item-l">
                    <img src="assets/product/header-product${linh_kien[Imgproduct-13].img}.jpg" alt="laptop" class="content__main--list-item-l-img">
                </div>
                <div class="content__main--left-list-item-r">
                    <span class="item-r-name">${linh_kien[Imgproduct-13].name}</span>
                    <br>
                    <input class="item-r-number" type="number" value="1" max = "10" min = "0">
                    <br>
                    <span class="item-r-price" >${linh_kien[Imgproduct-13].secondPrice}</span>
                    <button class="item-r-del">Xóa</button>
                </div>
            `

            ls.empty();
            ls.append(node);
            console.log(typeName, linh_kien[Imgproduct-13]);

        })
    }
    function eventUpdate(){
        $('.l__btn .btn').click(function(){
            let sum = 0;
            for(i = 0; i < $('.item-r-number').length; i++){
                let num = $(`.item-r-number:eq(${i})`).val();
                let price = $(`.item-r-price:eq(${i})`).text().toString();
                price = price.substr(0, price.length-2);
                price = price.replace(/,/g, '');
                sum += num*price;
            }
            sum = Number(sum);
            sum  = sum.toLocaleString('en');
            $('#tong_Tien').text(sum + 'đ');
        });
    }
});